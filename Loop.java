public class Loop 
{
    Loop()
    {
        //WHILE LOOP
        int i = 1;
        while (i <= 10 ) 
        {
           System.out.println(true); 
           i++;
        }

        //FOR LOOP
        
        for( int j = 0;j<= 5; j++)
        {
            System.out.println(j);
        }
    }
}
 